// isChinese reports whether the rune is a CJK Unified Ideograph.
func isChinese(r rune) bool {
	return (r >= 0x4E00 && r <= 0x9FFF) || // CJK Unified Ideographs
		(r >= 0x3400 && r <= 0x4DBF) || // CJK Unified Ideographs Extension A
		(r >= 0x20000 && r <= 0x2A6DF) || // CJK Unified Ideographs Extension B
		(r >= 0x2A700 && r <= 0x2B73F) || // CJK Unified Ideographs Extension C
		(r >= 0xF900 && r <= 0xFAFF) // CJK Compatibility Ideographs
}

// ExpandHomophones generates homophone variants for a Chinese word.
// It returns the original word plus homophone-substituted variants,
// limited to maxVariants total results (including the original).
//
// Strategy: convert each Chinese character in the word to its pinyin,
// then substitute each character with all characters that share the same
// pinyin reading. This produces a combinatorial expansion limited by
// maxVariants.
func ExpandHomophones(word string, maxVariants int) []string {
	if maxVariants <= 0 {
		maxVariants = 20
	}

	if word == "" {
		return nil
	}

	runes := []rune(word)
	py := ToPinyin(word)

	// Build character-level homophone options.
	// options[i] is the list of rune candidates for position i.
	// If the character is not Chinese or has no pinyin, only the original is kept.
	options := make([][]rune, len(runes))
	allChinese := true

	for i, r := range runes {
		if !isChinese(r) {
			allChinese = false
			options[i] = []rune{r}
			continue
		}

		if i < len(py) && len(py[i]) > 0 {
			syllable := py[i][0]
			candidates, ok := homophoneMap[syllable]
			if !ok || len(candidates) == 0 {
				options[i] = []rune{r}
				continue
			}
			// Convert candidates to runes.
			rs := make([]rune, 0, len(candidates)+1)
			rs = append(rs, r) // original first
			for _, c := range candidates {
				if c != string(r) {
					rs = append(rs, []rune(c)...)
				}
			}
			options[i] = rs
		} else {
			options[i] = []rune{r}
		}
	}

	// If no Chinese characters, return nil (no expansion possible).
	if !allChinese {
		return nil
	}

	// Generate combinations with iterative approach to limit growth.
	var results []string

	// Start with the original word.
	original := string(runes)
	results = append(results, original)

	// For each position, try substituting with homophones.
	for pos, candidates := range options {
		if len(candidates) <= 1 {
			continue
		}

		// Use existing results as base and substitute at this position.
		var newResults []string
		seen := make(map[string]bool, len(results)*len(candidates))

		for _, base := range results {
			baseRunes := []rune(base)
			for _, c := range candidates {
				if c == baseRunes[pos] {
					continue
				}
				variant := make([]rune, len(baseRunes))
				copy(variant, baseRunes)
				variant[pos] = c
				vs := string(variant)
				if !seen[vs] {
					seen[vs] = true
					newResults = append(newResults, vs)
				}
			}
		}

		// Stop if we've hit the limit.
		totalLen := len(results) + len(newResults)
		if totalLen > maxVariants {
			remaining := maxVariants - len(results)
			if remaining > 0 && remaining < len(newResults) {
				newResults = newResults[:remaining]
			}
		}

		results = append(results, newResults...)
		if len(results) >= maxVariants {
			break
		}
	}

	if len(results) > maxVariants {
		results = results[:maxVariants]
	}

	return results
}

// HomophoneMatch reports whether two Chinese strings have identical pinyin.
// This can be used for fuzzy matching beyond exact character substitution.
func HomophoneMatch(a, b string) bool {
	pa := ToPinyinStr(a)
	pb := ToPinyinStr(b)
	return pa == pb && a != b
}

// HasHomophoneVariant checks if a word has any homophone expansion at all.
func HasHomophoneVariant(word string) bool {
	variants := ExpandHomophones(word, 2)
	return len(variants) > 1
}

// FindPinyinSyllable returns the homophone character set for a single character's pinyin.
// Returns nil if the character is not Chinese or has no pinyin mapping.
func FindPinyinSyllable(r rune) []string {
	if !isChinese(r) {
		return nil
	}
	py := ToPinyin(string(r))
	if len(py) == 0 || len(py[0]) == 0 {
		return nil
	}
	syllable := py[0][0]
	return homophoneMap[syllable]
}
