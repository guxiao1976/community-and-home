// IgnoreChinese converts traditional Chinese characters to their simplified equivalents.
// Characters not in the mapping table are returned unchanged.
//
// Example:
//
//	'龜醫開關長門鐵電國黨廣廠壓歲塵僕價優償險隸雙雜離難雲霧靈靜韓韻響頁頂領' ->
//	'龟医开关长门铁电国党广厂压岁尘仆价优偿险隶双杂离难云雾灵静韩韵响页顶领'
func IgnoreChinese(c rune) rune {
	if s, ok := traditionalToSimplified[c]; ok {
		return s
	}
	return c
}
